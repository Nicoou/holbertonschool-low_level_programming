#include "3-calc.h"

/**
 * op_add - Sum two params.
 *
 * @a: First param.
 * @b: Second param.
 * Return: int.
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two params.
 *
 * @a: First param.
 * @b: Second param.
 * Return: int.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplicate two params.
 *
 * @a: First param.
 * @b: Second param.
 * Return: int.
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two params.
 *
 * @a: First param.
 * @b: Second param.
 * Return: int.
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder two params.
 *
 * @a: First param.
 * @b: Second param.
 * Return: int.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
